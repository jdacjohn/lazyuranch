	<h1>Location</h1>
	<p>The Lazy U Lodge is located 3.8 miles south of Interstate Highway 10 and 36 miles east of San Antonio, Texas - A nice stop-off for residents
  living near I-35 and east travelling to or from Houston or Dallas.</p>
  
  <h1>Reservations</h1>
  <p><strong>All Guests Must Make Reservations or Arrangements Before Arriving At the Ranch.  Without Them, You May 
  Drive Up To a Locked Gate.</strong></p>
  <p>When making reservations please indicate your estimated time of arrival. Unless otherwise indicated, check-in is at the Lazy U Lodge.
  We want to make sure that someone is around to greet you or that proper arrangements have been made for your arrival.</p>

