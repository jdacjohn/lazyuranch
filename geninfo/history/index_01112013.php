  	<p><strong>The Lazy U Ranch</strong> – The “U” stands for the Texas cattle brand registered by Henry Joseph Ulbrich in 1914.  
    Henry’s son James Jennings and Edie Ulbrich purchased the property in 1970.  Naumann Rd is named after the German family who 
    had a 360 acre land grant which included this property in the 1880s.  Several dwellings including those at the front gate are 
    still occupied by some of the Naumann descendants.</p>
    
  	<p><strong>The Ulbrich Family</strong> are Sixth Generation Texans.  August Karl Ulbrich arrived from Germany  in 1854 at the 
  	Gulf Coast port town of Indianola (later destroyed by a hurricane in 1886), traveled by foot a distance of 130 miles and settled 
  	the town of Martindale, sixteen miles northeast of the Lazy U Ranch. His original travel papers from Germany are displayed in the 
  	lodge entry.</p>
    
  	<p><strong>August Ulbrich</strong> (grandfather of Jim Ulbrich) is buried in a family cemetery.  His tombstone identifies his 
  	regiment in the Confederacy during the Civil War.  August Karl Ulbrich was born in the year of 1826.  He was 6 months old when 
  	Thomas Jefferson died, and 10 years old when the Alamo fell in 1836.</p>
    
		<p><strong>Today, James (Jim) Ulbrich</strong> and the majority of descendants were born and reside in and around the towns of 
  	Poteet and New Braunfels.  Edith (Voelger) Ulbrich’s ancestors arrived in the upper East Coast of the U.S. from Germany in 1914.  
  	She graduated from the University of Cincinnati in 1949, and brought a touch of “culture” to the Ulbrich family.  She passed away 
  	in 2003; a beautiful Texas pink granite headstone indicates her remains on the hill under our only mature post oak tree.  The 
  	Ulbrich family still keeps in touch with her German relatives.</p>
    
  	<p><strong>Jim Ulbrich</strong> is still alive and very active at the age of 85; he lives on the ranch with family and is still 
    involved in ranch improvements and plans.  A man of unusual stamina and strength, he single-handedly built the unfinished structure 
    called the “James Jennings Ulbrich” building near the lodge while in his 70s.  The building construction is similar of early 
    German “lime-crete” construction used in Seguin’s historic Sebastopol.  Unique to this building is that it is made of 100% crushed 
    limestone or locally known as “Texas road base”.  At this time it is the only know structure in existence built entirely of this 
    component.</p>
    
    <p><strong>Today</strong> the ranch is owned and operated by Jim’s daughter and son-in-law Nancy, Craig, and daughter Savanna Rio 
    Russell.  Craig himself a 5 generation Texan with family coming to Texas in the 1850’s has managed and operated the ranch as a 
    wildlife management area since 1998.  With extensive native grass management in place the small 101 acres hosts one of the most 
    robust native ecosystems in south Texas.</p>

