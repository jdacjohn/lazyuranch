  <h2>PARKING</h2>
	<p>Park in designated areas only. Use unloading area where designated, then move vehicle to parking area. Do not
  drive on lawn; septic tanks are buried there.</p>
    
  <h2>LIGHTS</h2>
	<p>When away from house, please do not leave on lights, stereo, ceiling fans, or appliances.</p>

  <h2>DOORS</h2>
	<p>Leave all outside doors closed at all times, even if you are gone only a little while! It takes only a moment
  for squirrels, raccoons, birds, rodents, etc., to enter the house.  You are responsible for damages if this occurs.</p>

  <h2>KITCHEN</h2>
	<p>In general, leave everything as you found it.</p>
	<p>Before departure, wash and put away all used cookware. Please wipe spills from refrigerator and countertops. Do
  NOT leave food behind.</p>
	<p>***Sweep the floor in kitchen and entry area and wipe or mop any spilled food!!! This will remove any bits of food
  that may attract bugs and critters! All cleaning supplies are located in closet in entry way. Not leaving the kitchen floor clean can 
  result in partial loss of deposit!</p>
 	<p>Kitchen is fully furnished. Do not bring additional cooking items such as electric griddles, etc. Please use kitchen
  rags, dish sponge, or paper towels rather than hand towels to clean. The cost of replacing soiled linens will be deducted from your
  security deposit.</p>
	<p>Automatic Coffee Pot: Do not add water! (Bring own coffee!) Place coffee filter in holder. (Use 
  filters in cabinet above coffee pot only.) Put ten (10!) tablespoons in filter. Turn on coffee warmer. Switch to "on" position (red).
  When green light comes on, push "brew" switch. Coffee will automatically fill pot to proper level.</p>
	<p><strong>Note:</strong> Hot water knob can be used for tea, hot chocolate, etc.</p>
	<p>Remember to turn off warmer!!!!</p>

  <h2>BBQ</h2>
	<p>Bar-B-Queuing is only allowed at the designated area. The barbeque grill is to stay near the fire pit.</p>
	
  <p>Please use a wire brush, or paper, or hand towels for cleaning grill, not household linens. The cost of 
  replacing soiled linens or towels will be deducted from your security deposit.</p>
	
  <p>When the coals and ashes are cooled down, empty into galvanized wash tub.  Please leave a clean BBQ pit for the next quest.</p>
 	
  <p>You must provide own starter fluid, charcoal, and matches.  Do not use any stacked firewood.</p>

  <h2>TELEPHONES</h2>
	<p>Bring your cell phones.  There is no land line at the house.</p>

	<h2>INDOOR / OUTDOOR FURNISHINGS</h2>
	<p>Return clean furnishings to original location. Do not take any inside furnishings outside of lodge.</p>
  
	<h2>TRASH</h2>
	<p>Take all bagged trash outside to red receptacle, keeping lid on tight.  Leave clean trashcans inside with
  replacement bags. A fee will be deducted from your security deposit if any trash is found on the ranch, or not tied in bags!</p>

	<h2>SPILLS</h2>
	<p>Use mop, brooms, sweeper, rags or paper towels for cleaning up spills.  Do not use wash cloths, kitchen towels,
  and / or bath towels. The cost of replacing the soiled linens will be deducted from your security deposit. All cleaning supplies are
  located in entryway closet. A professional cleaning company will be called in to clean any soiled carpets and / or furniture. All
  expenses will be deducted from your security deposit.</p>

	<h2>DIRTY LINEN</h2>
	<p> Before departure, place all used linens on the floor next to the washer/dryer located downstairs.  Please include all sheets, 
  bath towels, wash cloths, and kitchen rags.</p>

	<h2>CLEANING</h2>
	<p>Lazy U Ranch utilizes a maid service to sanitize bathrooms and kitchens and mop, etc., after your stay.  However,
