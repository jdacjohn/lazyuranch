  	<li>Walking is allowed ONLY on nature trails or main road.</li>
    <li>No fires are to be built on the property other than in the fire pit and barbeque pit.</li>
    <li>Nothing is to leave the property, such as rocks, plants, soil, wood, animals, etc.</li>
    <li>Absolutely no driving on lawn.</li>
