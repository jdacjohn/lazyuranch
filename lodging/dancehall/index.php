Named for Jim’s love for dancing, he can still cut a rug at age 86!  Etched in the concrete is the date (1943) of the building, names of family and barn yard animals.  Like most dance halls in south Texas, it is a basic structure with little or no flare for building design.  It is simply functional, but with lots of character. </p>

