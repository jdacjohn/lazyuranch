<p>Prudent management practices of native grasses and continual landscaping for improvement of native trees and cactus has lead to an extremely healthy and robust numbers of ALL types of wildlife during the worst drought in Texas history.   150 plus species of birds have been witnessed, photographed and/or documented.</p>

